// Define the data model
class Item {
  final String title;
  final String description;
  final String imageUrl;
  final String gender;
  final String type;
  final String id;

  Item({
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.gender,
    required this.type,
    required this.id,
  });
}
